# Este programa utiliza variables para almacenar valores
# y realizar operaciones básicas.
# -----------------------------------------------------------

# Crear variables con la cantidad de manzanas
john = 3   # Juan tiene 3 manzanas
mary = 5   # María tiene 5 manzanas
adam = 6   # Adán tiene 6 manzanas

# Imprimir las variables en una sola línea separadas por comas
print(john, mary, adam)

# Crear una variable con la suma total
total_apples = john + mary + adam

# Imprimir el total de manzanas
print("Total de manzanas:", total_apples)

# -----------------------------------------------------------
# EXPERIMENTACIÓN
# -----------------------------------------------------------

# Crear nuevas variables
extra_apples = 2

# Operaciones matemáticas
print("Suma:", total_apples + extra_apples)
print("Resta:", total_apples - extra_apples)
print("Multiplicación:", total_apples * 2)
print("División:", total_apples / 2)
print("División entera:", total_apples // 2)

# Concatenación de texto con número
print("Número total de manzanas:", total_apples)