# -----------------------------------------------------------
# LAB - Literales de Python (Cadenas)
# -----------------------------------------------------------
# Este programa muestra el uso de diferentes tipos de literales
# en Python, especialmente cadenas de texto.
#
# Un literal es un valor fijo que se escribe directamente
# en el código, como números o texto.
# -----------------------------------------------------------

# Ejemplo de cadena (string)
print("Hola mundo")

# Diferencia entre cadena y número
print("2")   # Esto es una cadena
print(2)     # Esto es un número entero

# Uso de comillas dentro de una cadena (carácter de escape \)
print("Me gusta \"Python\"")

# Uso de comillas simples
print('También puedo usar comillas simples')

# Uso de apóstrofe dentro de una cadena
print("I'm learning Python")

# Ejemplo de valores booleanos
print(True)   # Verdadero
print(False)  # Falso

# Comparación de valores booleanos
print(True > False)   # True equivale a 1, False a 0
print(True < False)