# Este programa muestra el uso de los operadores matemáticos
# básicos en Python y cómo funcionan en diferentes situaciones.
# -----------------------------------------------------------

# ---------------------------
# SUMA
# ---------------------------
# El operador + suma dos valores
print("Suma:", 2 + 2)

# ---------------------------
# RESTA
# ---------------------------
# El operador - resta dos valores
print("Resta:", 5 - 3)

# ---------------------------
# MULTIPLICACIÓN
# ---------------------------
# El operador * multiplica valores
print("Multiplicación:", 4 * 3)

# ---------------------------
# DIVISIÓN
# ---------------------------
# El operador / SIEMPRE devuelve un número flotante
print("División:", 10 / 2)

# ---------------------------
# DIVISIÓN ENTERA
# ---------------------------
# El operador // elimina la parte decimal
print("División entera:", 10 // 3)

# ---------------------------
# MÓDULO (RESIDUO)
# ---------------------------
# El operador % devuelve el residuo de una división
print("Módulo:", 10 % 3)

# ---------------------------
# POTENCIA
# ---------------------------
# El operador ** eleva un número a una potencia
print("Potencia:", 2 ** 3)

# ---------------------------
# PRIORIDAD DE OPERADORES
# ---------------------------
# Multiplicación tiene mayor prioridad que suma
print("Prioridad:", 2 + 3 * 5)

# ---------------------------
# USO DE PARÉNTESIS
# ---------------------------
# Los paréntesis cambian el orden de ejecución
print("Con paréntesis:", (2 + 3) * 5)

# ---------------------------
# EXPRESIÓN COMPLEJA
# ---------------------------
# Ejemplo con múltiples operadores
print("Expresión compleja:", (5 * ((25 % 13) + 100) / (2 * 13)) // 2)